<script>
    $(document).ready(function () {
        var size = '';
        var quantity = 1;
        var item = $('#item-name').html();
        var color = $('#color').html();
        var amount = $('#amount').html();
        $('.size').on('click', function () {
            $('.size').removeClass('active-size');
            $(this).addClass('active-size');
            size = $(this).data('size');
        });

        $('.add').on('click', function () {
            if(quantity >= 10){
                alert('You can only buy 10 items');
            }
            else{
                quantity = quantity + 1;
                $('.quantity').html(quantity);
            }
        });

        $('.remove').on('click', function () {
            if(quantity > 1){
                quantity = quantity - 1;
                $('.quantity').html(quantity);
            }
        });
        
        $('.cart').on('click', function () {
            $('.cart').html('<span class="fa fa-spinner"></span> adding...').attr('disabled','disabled');

            window.localStorage.clear();
            if(size){
                window.localStorage.setItem('size', size);
                window.localStorage.setItem('quantity', quantity);
                window.localStorage.setItem('color', color);
                window.localStorage.setItem('item', item);
                window.localStorage.setItem('amount', amount);
                setTimeout(function () {
                    $('.cart').html('Item added to cart').attr('disabled','disabled');
                    $('.notification').fadeIn(500);
                    window.location.href='cart.php';
                },1000);
            }
            else{
                alert('select size');
            }
        })
    })
</script>

